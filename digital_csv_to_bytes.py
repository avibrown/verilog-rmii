for clock, tx1, tx0 in filtered_data:
    # Interleave tx1 and tx0
    bitstream.extend([tx1, tx0])

# Step 4: Group bits into bytes
bytes_list = []
for i in range(0, len(bitstream), 8):
    byte_bits = bitstream[i:i+8]
    # Ensure we have 8 bits
    if len(byte_bits) < 8:
        break
    # Convert bits to a byte
    byte_value = 0
    for bit in byte_bits:
        byte_value = (byte_value << 1) | bit
    bytes_list.append(byte_value)

# Step 5: Convert bytes to hexadecimal representation
hex_stream = ' '.join(f'{byte:02X}' for byte in bytes_list)

# Step 6: Print out the binary stream in byte form
